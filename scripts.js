function getAboutLink(imageUrl) {
    const id = imageUrl.match(/\/(\d+)\//)[1]; 
    return `https://fortnite.gg/cosmetics?id=${id}`;
}

function displayNextSkin() {
    const skinContainer = document.getElementById('image-container');
    const skinImage = document.getElementById('fortnite-skin');
    const buttons = document.getElementById('buttons');
    
    if (currentIndex < skins.length) {
        skinImage.style.opacity = '0';

        setTimeout(() => {
            const imageUrl = skins[currentIndex];
            skinImage.src = imageUrl;

            setTimeout(() => {
                skinImage.style.opacity = '1';
                buttons.style.display = 'block'; // Show buttons when the new image is displayed
            }, 100);  

            const aboutButton = document.getElementById('about-skin-button') || document.createElement('button');
            aboutButton.id = 'about-skin-button';
            aboutButton.textContent = 'About this Skin';
            aboutButton.onclick = function() {
                window.open(getAboutLink(imageUrl), '_blank');
            };

            if (!document.getElementById('about-skin-button')) {
                skinContainer.appendChild(aboutButton);
            }
        }, 300); 
    } else {
        skinContainer.innerHTML = '<h1>No more skins left to rate!</h1>';
        buttons.style.display = 'none'; // Hide buttons when there are no more images
    }
}

function showHistory() {
    const historyDiv = document.getElementById('history');
    if (historyDiv.style.display === 'none') {
        historyDiv.style.display = 'block';
    } else {
        historyDiv.style.display = 'none';
    }
}

function clearHistory() {
    ensureAnonymousAuth().then(user => {
        const userVotesRef = firebase.database().ref('votes').orderByChild(user.uid);
        userVotesRef.once('value').then(snapshot => {
            snapshot.forEach(childSnapshot => {
                const childKey = childSnapshot.key;
                firebase.database().ref(`votes/${childKey}/${user.uid}`).remove();
            });
            localStorage.removeItem('skinRatings');
            localStorage.removeItem('currentIndex');
            currentIndex = 0;
            displayNextSkin();
            updateHistory();
            alert('Your voting history has been cleared.');
        }).catch(error => {
            console.error('Error clearing voting history:', error);
        });
    }).catch(error => {
        console.error('Error during authentication:', error);
    });
}

function updateHistory() {
    const historyDiv = document.getElementById('history');
    historyDiv.innerHTML = '';
    const ratings = JSON.parse(localStorage.getItem('skinRatings') || '{}');
    Object.keys(ratings).forEach(skin => {
        const container = document.createElement('div');

        const imageElement = document.createElement('img');
        imageElement.src = skin;
        container.appendChild(imageElement);

        const textElement = document.createElement('div');
        textElement.textContent = `Rated: ${ratings[skin]}`;
        textElement.className = ratings[skin] + 'Text'; 
        container.appendChild(textElement);

        historyDiv.insertBefore(container, historyDiv.firstChild);
    });

    if (Object.keys(ratings).length === 0) {
        historyDiv.innerHTML = '<p>No history to display.</p>';
    }
}