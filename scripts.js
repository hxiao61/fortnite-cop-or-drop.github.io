// Function to handle anonymous sign-in
function signInAnonymously() {
    return firebase.auth().signInAnonymously()
        .catch(error => {
            console.error('Error during anonymous sign-in:', error);
        });
}

// Ensure the user is signed in anonymously
function ensureAnonymousAuth() {
    return new Promise((resolve, reject) => {
        firebase.auth().onAuthStateChanged(user => {
            if (user) {
                console.log('User is signed in:', user.uid);
                resolve(user);
            } else {
                console.log('Signing in anonymously...');
                signInAnonymously().then(resolve).catch(reject);
            }
        });
    });
}

// Function to sanitize skin ID
function sanitizeSkinId(skin) {
    return skin.replace(/[^a-zA-Z0-9_-]/g, '_');
}

// Function to rate skin
function rateSkin(rating) {
    ensureAnonymousAuth().then(user => {
        if (currentIndex >= skins.length) return;

        const skin = skins[currentIndex];
        const currentData = localStorage.getItem('skinRatings') || '{}';
        const parsedData = JSON.parse(currentData);
        parsedData[skin] = rating;
        localStorage.setItem('skinRatings', JSON.stringify(parsedData));

        const skinId = sanitizeSkinId(skin);
        const userVoteKey = `votes/${skinId}/${user.uid}`;

        // Save vote to Firebase
        firebase.database().ref(userVoteKey).set(rating).then(() => {
            console.log(`Saved vote: ${rating} for skin: ${skinId} by user: ${user.uid}`);
            document.getElementById('buttons').style.display = 'none';
            showResults(skinId);
        }).catch(error => {
            console.error('Error saving vote:', error);
        });

        currentIndex++;
        localStorage.setItem('currentIndex', currentIndex.toString());
        displayNextSkin();
        updateHistory();
    }).catch(error => {
        console.error('Error during authentication:', error);
    });
}

function showResults(skinId) {
    const votesRef = firebase.database().ref(`votes/${skinId}`);
    votesRef.once('value').then(snapshot => {
        const votes = snapshot.val();
        if (votes) {
            const totalVotes = Object.keys(votes).length;
            const copVotes = Object.values(votes).filter(vote => vote === 'cop').length;
            const dropVotes = totalVotes - copVotes;

            console.log(`Results for skin: ${skinId}`);
            console.log(`Total votes: ${totalVotes}, Cop votes: ${copVotes}, Drop votes: ${dropVotes}`);

            document.getElementById('results').innerHTML = `
                <p>Cop: ${copVotes} (${(copVotes / totalVotes * 100).toFixed(2)}%)</p>
                <p>Drop: ${dropVotes} (${(dropVotes / totalVotes * 100).toFixed(2)}%)</p>
            `;
            document.getElementById('results').style.display = 'block';
        } else {
            console.log(`No votes found for skin: ${skinId}`);
            document.getElementById('results').innerHTML = `<p>No votes yet.</p>`;
            document.getElementById('results').style.display = 'block';
        }
    }).catch(error => {
        console.error('Error retrieving votes:', error);
    });
}

